//container for the red, green, and blue components of a color. 
public class Color {
	public int r;
	public int g;
	public int b;
	
	public Color(int r, int g, int b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
	public Color() {
		this(0, 0, 0);
	}
}
